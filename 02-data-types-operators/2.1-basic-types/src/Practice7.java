public class Practice7 {
    public static void main(String[] args) {
        System.out.println("Анализ кода из задания:\n");

        int a = 120;

        // Строка 1: byte b = a + 10;
        // ОШИБКА КОМПИЛЯЦИИ: тип int не может быть преобразован в byte
        // Причина: a + 10 дает результат типа int (130)
        // Решение: нужно явное приведение (byte)(a + 10)
        // byte b = a + 10; // Не скомпилируется!

        // Строка 2: byte c = (byte)(a + 10);
        // КОРРЕКТНО: явное приведение типа
        // a + 10 = 130 (int)
        // (byte)130 = -126 (переполнение)
        // Диапазон byte: -128..127, 130 > 127
        byte c = (byte)(a + 10); // c = -126

        // Строка 3: byte d = a + 1;
        // ОШИБКА КОМПИЛЯЦИИ: та же проблема
        // a + 1 = 121 (int), нужен (byte)
        // byte d = a + 1; // Не скомпилируется!

        // Правильный вариант для строки 3:
        byte e = (byte)(a + 1); // e = 121

        System.out.println("Итоговые значения:");
        System.out.println("c = (byte)(120 + 10) = " + c + " (было 130, стало -126)");
        System.out.println("e = (byte)(120 + 1) = " + e + " (121 помещается в byte)");
    }
}