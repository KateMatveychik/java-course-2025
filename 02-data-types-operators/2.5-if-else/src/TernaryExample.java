public class TernaryExample {
    public static void main(String[] args) {
        System.out.println("Замена if-else-if на тернарный оператор\n");

        int a = 2; // пример значения

        // Исходный код if-else-if:
        int i1 = 0;
        if (a == 1) {
            i1 = 1;
        } else if (a == 2) {
            i1 = 2;
        } else {
            i1 = 3;
        }
        System.out.println("if-else-if результат: i = " + i1);

        // Эквивалент через тернарный оператор:
        int i2 = (a == 1) ? 1 : (a == 2) ? 2 : 3;
        System.out.println("тернарный оператор: i = " + i2);

        /*
         * int i = (a == 1) ? 1 : (a == 2) ? 2 : 3;
         *
         * Работа тернарного оператора:
         * 1. Выражение (a == 1) ? 1 : X
         *    Если a == 1 истинно, возвращается 1
         *    Если ложно, возвращается X
         *
         * 2. X это второй тернарный оператор (a == 2) ? 2 : 3
         *    Если a == 2 истинно, возвращается 2
         *    Если ложно, возвращается 3
         *
         * Таким образом:
         * - При a == 1: первый тернарный оператор возвращает 1
         * - При a == 2: первый возвращает X, второй возвращает 2
         * - При других значениях: первый возвращает X, второй возвращает 3
         *
         * Это эквивалентно исходной конструкции if-else-if.
         */
    }
}